/*
Estrutura condicional if ... else

Ela funciona a partir de uma condição, se esta for verdadeira será executado um bloco de código; caso seja falsa poderá ser executado outro bloco de código.

Sintaxe:

if(condicao) {
    bloco comando verdadeiro
} else  {
    bloco comando falso
}

Aninhamento de if's:

if(condicao1) {
    bloco comando condição1
} else if(condicao2)  {
    bloco comando condição2
} else {
    bloco padrão
}
*/

